package Java.PBO.Praktikum.SortingBalls;

class Pemain {
    int tinggi, berat, noPemain;

    public Pemain(int noPemain, int tinggi, int berat) {
        this.noPemain = noPemain;
        this.tinggi = tinggi;
        this.berat = berat;
    }

    public int getTinggi() {
        return tinggi;
    }

    public int getBerat() {
        return berat;
    }

    public int getNoPemain() {
        return noPemain;
    }
}


